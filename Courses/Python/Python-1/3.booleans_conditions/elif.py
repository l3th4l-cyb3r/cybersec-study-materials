# we can use elif conditions to add alternative conditions to the control flow
score = 65
if score == 100:
  print("Great Job!")
elif score >= 60:
  print("Do more hardwork and you will reach to the top!")
else:
  print("You have to do better man! What the hell are you doing?!")

# Control flow stops as soon as a condition returns true
# We can add as many "elifs" as we want